#include "push_swap.h"

void	pa(t_stack **a, t_stack **b)
{
	t_stack	**first1;
	t_stack	**first2;

	first1 = *a;
	first2 = *b;
	if (!b || !*b)
		return ;
	
}

void	pb(t_stack **a, t_stack **b)
{
	t_stack	**first1;
	t_stack	**first2;

	first1 = *a;
	first2 = *b;
	if (!a || !*a)
		return ;
	
}
