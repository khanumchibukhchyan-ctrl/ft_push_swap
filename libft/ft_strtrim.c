#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	end;
	size_t	start;
	char	*new;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	new = (char *)malloc(end - start + 1);
	if (!new)
		return (NULL);
	ft_memcpy(new, s1 + start, end - start);
	new[end - start] = '\0';
	return (new);
}
